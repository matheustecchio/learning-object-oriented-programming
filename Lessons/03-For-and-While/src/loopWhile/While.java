package loopWhile;

public class While {
	
	public static void main(String [] args) {
		
	int i=1;                                            // Defining the value of the variable i
	
	while(i<10) {                                      	// Creating the logical repeat test */
		System.out.println("The value of i is "+i);
		i++;                                            // Add +1 to the variable
		System.out.println("Now the value of i is "+i);
		System.out.println("");
	}
	}
}
